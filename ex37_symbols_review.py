# import        # import module into this one to use                    # import os
# in            # part of for-loops. Also a test of X in Y              # 1) for X in Y: pass      2) 1 in [1] == True
# is            # Like == to test equality                              # 1 is 1 == True
# lambda        # Create a short anonymous function                     # s = lambda y: y ** y;   s(3)
